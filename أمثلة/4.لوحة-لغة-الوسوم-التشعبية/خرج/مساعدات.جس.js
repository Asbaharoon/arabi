export function عشو(بداية, نهاية) {
  return رياضيات.عشوائي() * (نهاية - بداية) + بداية;
}

export function صحيح_عشو(بداية, نهاية) {
  return رياضيات.قرب(عشو(بداية, نهاية));
}

export function ليس_رقما(ر) {
  return typeof ر !== 'number';
}
